Cek petunjuk lengkapnya di video ini</p>


<h3><b>f. Cara Bayar Belanja Online Pakai Sakuku</b></h3>

<p>Klik “Uang Elektronik” pada kolom metode pembayaran, lalu pilih Sakuku
Kilk “Bayar Sekarang” agar diarahkan otomatis ke halaman Sakuku
Login di halaman Sakuku menggunakan nomor handphone yang terdaftar
Dapatkan QR Code setelah proses login selesai
Buka aplikasi Sakuku melalui smartphone kemudian pilih “Bayar Belanja”
Scan QR Code menggunakan smartphone
Jika scanning berhasil, masuk ke laman “Konfirmasi Transaksi”
Klik tombol “Lanjut” untuk membayar nominal transaksi
Transaksi selesai
Cek petunjuk lengkapnya di video ini</p>


